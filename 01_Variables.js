//  4 ways to declare js variable

//  1) using var

var a = 10;
console.log(a);

// 2) using let

let b = 10;
console.log(b);

// 3) using const

const c = 10;
console.log(c);

// 4) direct
// -> Here js compiler automatically create var d=10 if variable is directly declared

d = 10;
console.log(d);
